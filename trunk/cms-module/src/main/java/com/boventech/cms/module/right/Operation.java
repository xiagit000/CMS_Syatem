package com.boventech.cms.module.right;

public enum Operation {
	
	//ADD_NODE("addNode", 1),
	
	EDIT_NODE("editNode", 2),
	
	DELETE_NODE("deleteNode", 3),
	
	ADD_ENTRY("addEntry", 4),
	
	EDIT_ENTRY("editEntry", 5),
	
	DELETE_ENTRY("deleteEntry", 6);

	private String name;
	
	private int position;

	private Operation(String name, int position){
		this.name = name;
		this.position = position;
	}
	
	public String getName() {
		return name;
	}
	
	public int getPosition() {
		return position;
	}
	
	public String getValue(){
		return this.toString();
	}
}
