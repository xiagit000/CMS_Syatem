package com.boventech.cms.service.register;

import java.util.List;

import com.boventech.cms.module.register.Register;
import com.boventech.cms.module.web.PageIndex;

public interface RegisterService {

	/**
	 * Register a new student.
	 * 
	 * @param register
	 */
	Register register(Register register);

	/**
	 * List registers by pageIndex.
	 * 
	 * @param pageIndex
	 * @return 
	 */
	List<Register> list(PageIndex pageIndex);

	/**
	 * Remove a register by its id.
	 * 
	 * @param id
	 */
	void removeRegister(Long id);

	/**
	 * Find Register by ID.
	 * 
	 * @param id
	 * @return
	 */
	Register find(Long id);
	
}
